import java.util.Scanner;

public class Task1 {
    public static void main(String[] args) {
        // Создаем объект Scanner для ввода данных с клавиатуры
        Scanner scanner = new Scanner(System.in);

        // Запрашиваем у пользователя основание системы счисления k
        System.out.println("Введите основание системы счисления k (2 < k < 9):");
        int k = scanner.nextInt();

        // Проверяем, что k находится в допустимом диапазоне
        if (k <= 2 || k >= 9) {
            System.out.println("Ошибка: k должно быть в диапазоне 2 < k < 9.");
            return; // Завершаем программу, если k не соответствует условию
        }

        // Запрашиваем у пользователя число n в системе счисления с основанием k
        System.out.println("Введите число n в системе счисления с основанием k:");
        int n = scanner.nextInt();

        // Инициализируем переменную для хранения результата в десятичной системе
        int decimal = 0;
        // Инициализируем переменную для хранения текущей степени основания k
        int power = 0;

        // Преобразуем число n из системы счисления с основанием k в десятичную систему
        while (n > 0) {
            // Получаем последнюю цифру числа n
            int digit = n % 10;

            // Проверяем, что цифра не превышает основание системы счисления
            if (digit >= k) {
                System.out.println("Ошибка: цифра числа n не может быть больше или равна основанию системы.");
                return; // Завершаем программу, если цифра недопустима
            }

            // Добавляем к результату значение текущей цифры, умноженное на k в степени power
            decimal += digit * Math.pow(k, power);

            // Убираем последнюю цифру из числа n
            n /= 10;

            // Увеличиваем степень для следующего разряда
            power++;
        }

        // Выводим результат преобразования
        System.out.println("Число в десятичной системе: " + decimal);
    }
}