import java.util.Scanner;

public class Task2 {
    public static void main(String[] args) {
        // Создаем объект Scanner для ввода данных с клавиатуры
        Scanner scanner = new Scanner(System.in);

        // Запрашиваем у пользователя длину последовательности
        System.out.println("Введите длину последовательности:");
        int n = scanner.nextInt();

        // Проверяем, что длина последовательности не меньше 3
        if (n < 3) {
            System.out.println("Последовательность должна быть длиной не менее 3.");
            return; // Завершаем программу, если длина последовательности меньше 3
        }

        // Создаем массив для хранения последовательности чисел
        int[] numbers = new int[n];

        // Запрашиваем у пользователя ввод последовательности чисел
        System.out.println("Введите последовательность чисел:");
        for (int i = 0; i < n; i++) {
            numbers[i] = scanner.nextInt();
        }

        // Инициализируем счетчик для подсчета четных локальных максимумов
        int evenLocalMaxCount = 0;

        // Проходим по последовательности, начиная со второго элемента и заканчивая предпоследним
        for (int i = 1; i < n - 1; i++) {
            // Проверяем, является ли текущий элемент локальным максимумом и четным числом
            if (numbers[i] > numbers[i - 1] && numbers[i] > numbers[i + 1] && numbers[i] % 2 == 0) {
                evenLocalMaxCount++; // Увеличиваем счетчик, если условие выполняется
            }
        }

        // Проверяем, сколько четных локальных максимумов было найдено
        if (evenLocalMaxCount == 2) {
            System.out.println("Ровно два чётных локальных максимума существуют.");
        } else {
            System.out.println("Чётных локальных максимумов не ровно два.");
        }
    }
}